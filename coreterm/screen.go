type Instance struct {
	Border        int
	Height        int
	Width         int
	Scale         float64
	CurrentColor  byte
	uTime         int
	updateScreen  bool
	tmpScreen     *ebiten.Image
	img           *image.RGBA
	ScreenHandler func(*Instance) error
	Title         string
	Font          struct {
		Height int
		Width  int
		Bitmap []byte
	}
}

var ct *Instance

func Get() *Instance {
	ct = &Instance{}
	ct.Width = columns * 9
	ct.Height = rows * 16
	ct.Scale = 1
	ct.ScreenHandler = update
	ct.Title = "term"
	ct.CurrentColor = 0x0
	return ct
}

