func MergeColorCode(b, f byte) byte {
	return (f & 0xff) | (b << 4)
}

func updateTermScreen(screen *ebiten.Image) error {

	if ct.ScreenHandler != nil {
		err := ct.ScreenHandler(ct)
		if err != nil {
			return err
		}
	}

	if ct.updateScreen {
		ct.tmpScreen.ReplacePixels(ct.img.Pix)
		ct.updateScreen = false
	}

	screen.DrawImage(ct.tmpScreen, nil)
	ct.uTime++
	return nil
}

func (p *Instance) Run() {
	{
		p.Font.Bitmap = bitmap
		p.Font.Height = 16
		p.Font.Width = 9
	}

	p.img = image.NewRGBA(image.Rect(0, 0, p.Width, p.Height))
	p.tmpScreen, _ = ebiten.NewImage(p.Width, p.Height, ebiten.FilterNearest)

	p.Clear()
	p.updateScreen = true
	clearVideoTextMode()

	if err := ebiten.Run(updateTermScreen, p.Width, p.Height, p.Scale, p.Title); err != nil {
		log.Fatal(err)
	}
}

func (p *Instance) DrawPix(x, y int) {
	x += p.Border
	y += p.Border
	if x < p.Border || y < p.Border || x >= p.Width-p.Border || y >= p.Height-p.Border {
		return
	}
	pos := 4*y*p.Width + 4*x
	p.img.Pix[pos] = Colors[p.CurrentColor].R
	p.img.Pix[pos+1] = Colors[p.CurrentColor].G
	p.img.Pix[pos+2] = Colors[p.CurrentColor].B
	p.img.Pix[pos+3] = 0xff
	p.updateScreen = true
}

func (p *Instance) DrawChar(index, fgColor, bgColor byte, x, y int) {
	var a uint
	var b uint
	var lColor byte
	for b = 0; b < 16; b++ {
		for a = 0; a < 9; a++ {
			i := uint(index)*16 + b
			if a == 8 {
				p.CurrentColor = bgColor
				if index>=192 && index<=223 {
					p.CurrentColor = lColor
				}
				p.DrawPix(int(a)+x, int(b)+y)
				continue
			}
			if bitmap[i]&(0x80>>a) != 0 {
				p.CurrentColor = fgColor
				lColor = fgColor
				p.DrawPix(int(a)+x, int(b)+y)
				continue
			}
			p.CurrentColor = bgColor
			lColor = bgColor
			p.DrawPix(int(a)+x, int(b)+y)
		}
	}
}

func (p *Instance) Clear() {
	for i := 0; i < p.Height*p.Width*4; i += 4 {
		p.img.Pix[i] = Colors[p.CurrentColor].R
		p.img.Pix[i+1] = Colors[p.CurrentColor].G
		p.img.Pix[i+2] = Colors[p.CurrentColor].B
		p.img.Pix[i+3] = 0xff
	}
}

func (p *Instance) DrawCursor(index, fgColor, bgColor byte, x, y int) {
	if cursorSetBlink {
		if cursorBlinkTimer < 15 {
			fgColor, bgColor = bgColor, fgColor
		}
		p.DrawChar(index, fgColor, bgColor, x, y)
		cursorBlinkTimer++
		if cursorBlinkTimer > 30 {
			cursorBlinkTimer = 0
		}
		return
	} 
	p.DrawChar(index, bgColor, fgColor, x, y)
}

func (p *Instance) DrawVideoTextMode() {
	i := 0
	for r := 0; r < rows; r++ {
		for c := 0; c < columns; c++ {
			color := videoTextMemory[i]
			f := color & 0x0f
			b := color & 0xf0 >> 4
			if i == cursor {
				i++
				p.DrawCursor(videoTextMemory[i], f, b, c*9, r*16)
			} else {
				i++
				p.DrawChar(videoTextMemory[i], f, b, c*9, r*16)
			}
			i++
		}
	}
}

func clearVideoTextMode() {
	copy(videoTextMemory[:], make([]byte, len(videoTextMemory)))
	for i := 0; i < len(videoTextMemory); i += 2 {
		videoTextMemory[i] = currentColor
	}
}

func moveLineUp() {
	copy(videoTextMemory[0:], videoTextMemory[columns*2:])
	copy(videoTextMemory[len(videoTextMemory)-columns*2:], make([]byte, columns*2))
	for i := len(videoTextMemory) - columns*2; i < len(videoTextMemory); i += 2 {
		videoTextMemory[i] = currentColor
	}
}

func correctVideoCursor() {
	if cursor < 0 {
		cursor = 0
	}
	for cursor >= rows*columns*2 {
		cursor -= columns * 2
		moveLineUp()
	}
}

func putChar(c byte) {
	correctVideoCursor()
	videoTextMemory[cursor] = currentColor
	cursor++
	correctVideoCursor()
	videoTextMemory[cursor] = c
	cursor++
	correctVideoCursor()
}

func bPrint(msg string) {
	for i := 0; i < len(msg); i++ {
		c := msg[i]

		switch c {
		case 13:
			cursor += columns * 2
			continue
		case 10:
			aux := cursor / (columns * 2)
			aux = aux * (columns * 2)
			cursor = aux
			continue
		}
		putChar(msg[i])
	}
}

func bPrintln(msg string) {
	msg += "\r\n"
	bPrint(msg)
}

func keyTreatment(c byte, f func(c byte)) {
	if noKey || lastKey.Char != c || lastKey.Time+20 < uTime {
		f(c)
		noKey = false
		lastKey.Char = c
		lastKey.Time = uTime
	}
}

func getLine() string {
	aux := cursor / (columns * 2)
	var ret string
	for i := aux*(columns*2) + 1; i < aux*(columns*2)+columns*2; i += 2 {
		c := videoTextMemory[i]
		if c == 0 {
			break
		}
		ret += string(videoTextMemory[i])
	}

	ret = strings.TrimSpace(ret)
	return ret
}

